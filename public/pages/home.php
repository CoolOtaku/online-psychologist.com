            </p>
            <p><i>Психічне</i> (його природа, межі, принципи існування) все ще є <i>terra incognita</i> для наукового знання та постійно стає простором спекуляцій і містифікацій з боку шарлатанів. <i>Психічне</i> залишається також у центрі уваги релігій, які стверджують його універсальність та примат відносно фізичного світу. <i>Психічне</i> є унікальною цариною, в якій живемо всі ми; незалежно від віку, статі, професії, освіти&nbsp;— кожен з нас є «психологом» на побутовому рівні.
            </p>
            <p>Психологічне знання є основою специфічних практик. Традиційно <a href="https://uk.m.wikipedia.org/wiki/%D0%9F%D1%80%D0%B8%D0%BA%D0%BB%D0%B0%D0%B4%D0%BD%D0%B0_%D0%BF%D1%81%D0%B8%D1%85%D0%BE%D0%BB%D0%BE%D0%B3%D1%96%D1%8F" title="Прикладна психологія">прикладною психологією</a> називають галузі науки зосереджені на застосуванні психологічних знань у конкретних сферах діяльності (музична, політична психологія, психологія освіти або праці); <a href="https://uk.m.wikipedia.org/wiki/%D0%9F%D1%80%D0%B0%D0%BA%D1%82%D0%B8%D1%87%D0%BD%D0%B0_%D0%BF%D1%81%D0%B8%D1%85%D0%BE%D0%BB%D0%BE%D0%B3%D1%96%D1%8F" title="Практична психологія">практична психологія</a>&nbsp;— це галузь знань та досліджень щодо форм та методів використання психологічних знань фахівцями-психологами (консультантами, тренерами). Практична психологія поділяється на <a href="https://uk.m.wikipedia.org/wiki/%D0%9F%D1%81%D0%B8%D1%85%D0%BE%D0%B4%D1%96%D0%B0%D0%B3%D0%BD%D0%BE%D1%81%D1%82%D0%B8%D0%BA%D0%B0" title="Психодіагностика">психодіагностику</a>, <a href="https://uk.m.wikipedia.org/wiki/%D0%9F%D1%81%D0%B8%D1%85%D0%BE%D0%BB%D0%BE%D0%B3%D1%96%D1%87%D0%BD%D0%B5_%D0%BA%D0%BE%D0%BD%D1%81%D1%83%D0%BB%D1%8C%D1%82%D1%83%D0%B2%D0%B0%D0%BD%D0%BD%D1%8F" title="Психологічне консультування">консультування</a>, <a href="https://uk.m.wikipedia.org/wiki/%D0%9F%D1%81%D0%B8%D1%85%D0%BE%D0%BB%D0%BE%D0%B3%D1%96%D1%87%D0%BD%D0%B0_%D0%BA%D0%BE%D1%80%D0%B5%D0%BA%D1%86%D1%96%D1%8F" title="Психологічна корекція">корекцію</a>, <a href="https://uk.m.wikipedia.org/wiki/%D0%9F%D1%81%D0%B8%D1%85%D0%BE%D0%BB%D0%BE%D0%B3%D1%96%D1%87%D0%BD%D0%B0_%D0%B4%D0%BE%D0%BF%D0%BE%D0%BC%D0%BE%D0%B3%D0%B0" title="Психологічна допомога">психологічну допомогу</a>.
            </p>

            <h2>20 цікавих фактів про психологію людини</h2>
            <p>Всім нам відомо, що <em>психологія</em> — це наука про людину.&nbsp; Об’єктом цієї науки є найскладніша сфера життєдіяльності людини – психіка. Складність психіки як явища зумовлена тим, що вона є вищим продуктом біологічного та суспільного розвитку живих істот. (С.Д. Максименко)</p>
            <p>Психологію можна віднести до одної з найцікавіших наук. Якщо Вам все ж хочеться дізнатися більше, то цікаві факти про психологію Вам допоможуть.</p>
            <p><strong>№1</strong>: Мозкова діяльність продовжується навіть тоді, коли ми відпочиваємо. У цей момент відбувається фільтрація того, що дійсно потрібно відкласти в пам’яті, а що прибрати «про запас». Прокинувшись вранці ви ніколи не забудете те, що дійсно важливо для вас, але і не згадайте того, на чому не зупинили свою увагу вчора.</p>
            <p><strong>№2</strong>: Людина відчуває себе більш успішною та щасливим тільки тоді, коли її мозок чимось зайнятий. Більш того, сіра речовина не цікавить монотонна робота – мова йде як раз про постійну метушню концентрації та перемикання з однієї роботи на іншу. Тільки в такі моменти людина відчуває себе щасливою.</p>
            <p><strong>№3</strong>: Дослідники довели, що якщо в юності людина вміла відмовлятися від того, чого сильно бажала, їй даються життєві випробування простіше та з мінімальними втратами.</p>
            <p><strong>№4</strong>: Для того, щоб до чогось звикнути необхідно трохи більше 2-х місяців, а точніше – 66 днів. Саме такий період знадобиться людині для того, щоб сформувати та довести до автоматизму будь-яку дію. Наприклад, якщо ви хочете перейти на правильне харчування, то вам може знадобитися трохи менше – приблизно 55 днів. А ось до спорту доведеться звикати довше – до 75 днів.</p>
            <p><strong>№5</strong>: Якщо ви думаєте, що можете мати безмежну кількість друзів, ви глибоко помиляєтеся. Виявляється, що людина за все своє життя може дружити до 150 разів.</p>
            <p><strong>№6</strong>: Якщо вас мучать кошмари по ночах, можливо, ви мерзнете уві сні. Науковий факт – чим холодніше в спальні, тим більше шансів побачити поганий сон.</p>
            <p><strong>№7</strong>: Кращим засобом від турбот, тривожності і стресу є читання. Це заняття надає більш швидкий ефект на організм. Дивно, але цей спосіб багато краще прийому алкоголю, набагато ефективніше прогулянкам, чаювання або прослуховування музики.</p>
            <p><strong>№8</strong>: У психології є принцип: чим більше очікувань з приводу якоїсь події, тим вище ймовірність нищівної розчарування. Більше чекаєш – менше отримуєш, менше чекаєш – більше отримуєш.</p>
            <p><strong>№9</strong>: Більшість людей в незнайомому місці тримається праворуч. Якщо ви не хочете бути в натовпі або простояти довго в черзі, то знаючи цей факт, сміливо йдіть ліворуч або займайте чергу, яка лівіше.</p>
            <p><strong>№10</strong>: Цікаво те, що хороші оцінки людина запам’ятовує у багато разів краще, ніж погані. З імовірністю 89% люди згадають позначку «5» і лише 29% – оцінку «3». Як наслідок, здається, що оцінка була вище, ніж насправді.</p>
            <p><strong>№11</strong>: Цікаво, що судді в спорті карають частіше ті команди, форма яких чорного кольору. Це підтверджує статистика, яку ведуть НХЛ, ФІФА.</p>
            <p><strong>№12</strong>: Відомий факт, що жінки, перебуваючи в магазині одягу, більше акцентують увагу на полиці та вішалки, на яких панує безлад. Підсвідомо їм здається, що саме там знаходиться щось краще, цікавіше.</p>
            <p><strong>№13</strong>: Цілу сферу психологічних факторів та прийомів використовують на собі магазини. Наприклад, повторювані товари на касі, акції та спеціальні цінники різних кольорів. Якщо на ціннику написати замість «Шкарпетки – 2 грн.» – «АКЦІЯ! 5 пар носків – 10 грн.» продажі можна збільшити рівно наполовину.</p>
            <p><strong>№14</strong>: Люди неохоче суперечать спокійній, врівноваженій людині. Навпаки, коли хтось люто і агресивно відстоює свою думку – йому чинитимуть опір та будуть з ним сперечатися.</p>
            <p><strong>№15</strong>: Ті, хто спить 6-7 годин, менше схильні до ризику передчасної смерті, ніж ті, хто спить по 8 годин. Варто відмітити, що ті, хто спить менше 5 годин – в три рази частіше страждають психічними розладами, ніж ті, хто спить по 8-9 годин.</p>
            <p><strong>№16</strong>: Немає для людини кращого слова, ніж її ім’я. Перше, що треба запам’ятовувати при знайомстві – ім’я. Не посаду, не професію, а ім’я. Це основне правило встановлення хороших відносин.</p>
            <p><strong>№17</strong>: Якщо людина намагається щось пригадати, але при цьому продовжує дивитися вам в очі, будьте упевнені, вас обманюють.</p>
