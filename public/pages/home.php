            <p><i><a href="https://uk.m.wikipedia.org/wiki/%D0%9F%D1%81%D0%B8%D1%85%D1%96%D1%87%D0%BD%D0%B5" title="Психічне">Психічне</a></i>&nbsp;— філософська категорія, що відбиває явища як людського, так і тваринного, навіть, рослинного світу. <a href="https://uk.m.wikipedia.org/wiki/%D0%97%D0%BE%D0%BE%D0%BF%D1%81%D0%B8%D1%85%D0%BE%D0%BB%D0%BE%D0%B3%D1%96%D1%8F" title="Зоопсихологія">Зоопсихологію</a>, яка досліджує явища <a href="https://uk.m.wikipedia.org/wiki/%D0%95%D0%BB%D0%B5%D0%BC%D0%B5%D0%BD%D1%82%D0%B0%D1%80%D0%BD%D0%B0_%D0%BF%D1%81%D0%B8%D1%85%D1%96%D0%BA%D0%B0" title="Елементарна психіка">елементарної психіки</a>, поведінки, пізнавальних процесів у тварин, відносять як до психологічних, так і до <a href="https://uk.m.wikipedia.org/wiki/%D0%91%D1%96%D0%BE%D0%BB%D0%BE%D0%B3%D1%96%D1%87%D0%BD%D1%96_%D0%BD%D0%B0%D1%83%D0%BA%D0%B8" class="mw-redirect" title="Біологічні науки">біологічних наук</a>. Коли йде мова про <i>психіку <a href="https://uk.m.wikipedia.org/wiki/%D0%9B%D1%8E%D0%B4%D0%B8%D0%BD%D0%B0" title="Людина">людини</a></i>, притаманні їй <a href="https://uk.m.wikipedia.org/wiki/%D0%9F%D1%81%D0%B8%D1%85%D1%96%D1%87%D0%BD%D1%96_%D1%8F%D0%B2%D0%B8%D1%89%D0%B0" title="Психічні явища">явища</a>, слід пам'ятати, що вона структурно та якісно відмінна від своїх еволюційних попередниць.
            </p>
